                    <input type="text" :placeholder="obj.placeholder" v-model="searchWord" @keydown.enter="searchAll()" />
                    <div @click="searchAll">全网搜</div>
                    <div @click="searchShop">搜本店</div>
                </div>
            </div>
        </div>
    </div>
</template>

<script>
    import publicUtil from "../../../common/publicUtil";
    export default {
        name: "head2",
        props:['obj'],
        data(){
            return {
                headImg:'/shop_web/shop/images/zhutiLogo.png',
                name:'',
                searchWord:null,
                entrepId:null,
            }
        },
        created(){
            this.entrepId = this.$route.params.entrepId;
            this.searchWord = this.obj.searchWord;
            this.$axios.get(this.interfaceApi.getEntrepInfo,{params:{id:this.entrepId}}).then((res)=>{
                if(res.status === 200){
                    let data = res.data;
                    console.log(data);
                    if(data.status === 0 && data.data){
                        this.name = data.data.fName;
                        this.$store.commit("changeEntrepInfo",data.data);
                        if(data.data.fLogo){
                            this.headImg = data.data.fLogo;
                        }
                    }
                }
            },(err)=>{
                console.log(err);
            });
        },
        methods:{
            handleImg(e,maxWidth,maxHeight){
                publicUtil.handleImg(e,maxWidth,maxHeight);
            },
            searchAll(){
                switch (this.obj.module) {
                    case 'gouwu':
                        window.location.href = this.productUrl1;
                        break;
                    case 'fuwu':
                        window.location.href = this.serverUrl1;
                        break;
                    case 'zixun':
                        window.location.href = this.zixunUrl1;
                        break;
                    case 'wenzhang':
                        window.location.href = this.wenzhangUrl1;
                        break;
                }
            },
            searchShop(){
                switch (this.obj.module) {
                    case 'gouwu':
                        window.location.href = this.productUrl;
                        break;
                    case 'fuwu':
                        window.location.href = this.serverUrl;
                        break;
                    case 'zixun':
                        window.location.href = this.zixunUrl;
                        break;
                    case 'wenzhang':
                        window.location.href = this.wenzhangUrl;
                        break;
                }
            },
        },
        computed:{
            productUrl(){
                if(this.searchWord !== null && this.searchWord.trim()){
                    return `/${this.entrepId}/gwsousuo/${encodeURI(this.searchWord)}`;
                }else{
                    if(this.searchWord !== null && this.searchWord.trim() === ""){
                        return `/${this.entrepId}/gwsousuo`;
                    }else{
                        return `/${this.entrepId}/gouwu`
                    }
                }
            },
            serverUrl(){
                if(this.searchWord !== null && this.searchWord.trim()){
                    return `/${this.entrepId}/fwsousuo/${encodeURI(this.searchWord)}`;
                }else{
                    if(this.searchWord !== null && this.searchWord.trim() === ""){
                        return `/${this.entrepId}/fwusuo`;
                    }else{
                        return `/${this.entrepId}/fuwu`
                    }
                }
            },
            zixunUrl(){
                if(this.searchWord !== null && this.searchWord.trim()){
                    return `/${this.entrepId}/zxsousuo/${encodeURI(this.searchWord)}`;
                }else{
                    if(this.searchWord !== null && this.searchWord.trim() === ""){
                        return `/${this.entrepId}/zxsousuo`;
                    }else{
                        return `/${this.entrepId}/zixun`
                    }
                }
            },
            wenzhangUrl(){
                if(this.searchWord !== null && this.searchWord.trim()){
                    return `/${this.entrepId}/wzsousuo/${encodeURI(this.searchWord)}`;
                }else{
                    if(this.searchWord !== null && this.searchWord.trim() === ""){
                        return `/${this.entrepId}/wzsousuo`;
                    }else{
                        return `/${this.entrepId}/wenzhang`
                    }
                }
            },
            productUrl1(){
                if(this.searchWord !== null && this.searchWord.trim()){
                    return `/gwsousuo/${encodeURI(this.searchWord)}`;
                }else{
                    if(this.searchWord !== null && this.searchWord.trim() === ""){
                        return `/gwsousuo`;
                    }else{
                        return `/gouwu`
                    }
                }
            },
            serverUrl1(){
                if(this.searchWord !== null && this.searchWord.trim()){
                    return `/fwsousuo/${encodeURI(this.searchWord)}`;
                }else{
                    if(this.searchWord !== null && this.searchWord.trim() === ""){
                        return `/fwsousuo`;
                    }else{
                        return `/fuwu`
                    }
                }
            },
            zixunUrl1(){
                if(this.searchWord !== null && this.searchWord.trim()){
                    return `/zxsousuo/${encodeURI(this.searchWord)}`;
                }else{
                    if(this.searchWord !== null && this.searchWord.trim() === ""){
                        return `/zxsousuo`;
                    }else{
                        return `/zixun`
                    }
                }
            },
            wenzhangUrl1(){
                if(this.searchWord !== null && this.searchWord.trim()){
                    return `/wzsousuo/${encodeURI(this.searchWord)}`;
                }else{
                    if(this.searchWord !== null && this.searchWord.trim() === ""){
                        return `/wzsousuo`;
                    }else{
                        return `/wenzhang`
                    }
                }
            }
        },
        watch: {
            searchWord(newVal, oldVal) {
                if (newVal) {
                    let len = 0;
                    for (let i = 0; i < newVal.length; i++) {
                        let length = newVal.charCodeAt(i);
                        if (length >= 0 && length <= 128) {
                            len += 1;
                        }
                        else {
                            len += 2;
                        }
                    }
                    if (len > 50) {
                        this.searchWord = oldVal;
                    } else {
                        this.searchWord = newVal;
                    }
                } else {
                    if (newVal === "") {
                        this.searchWord = null;
                    }
                }
            }
        }
    }
</script>

